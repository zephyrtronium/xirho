// Most of this file is copied from github.com/zephyrtronium/crazy.

// RNG is the randomness source type for xirho, an implementation of
// xoshiro256** by David Blackman and Sebastiano Vigna.
type RNG struct {
	w, x, y, z uint64
}

// NewRNG produces a new, uniquely seeded RNG.
func NewRNG() RNG {
	r := RNG{}
	b := make([]byte, 8*4)
	// Loop to ensure the state is never all-zero, which is an invalid state
	// for xoshiro.
	for r.w == 0 && r.x == 0 && r.y == 0 && r.z == 0 {
		rand.Read(b)
		r.w = binary.LittleEndian.Uint64(b)
		r.x = binary.LittleEndian.Uint64(b[8:])
		r.y = binary.LittleEndian.Uint64(b[16:])
		r.z = binary.LittleEndian.Uint64(b[24:])
	}
	return r
}

// Uint64 produces a 64-bit pseudo-random value.
func (rng *RNG) Uint64() uint64 {
	w, x, y, z := rng.w, rng.x, rng.y, rng.z
	r := bits.RotateLeft64(x*5, 7) * 9
	t := x << 17
	y ^= w
	z ^= x
	x ^= y
	w ^= z
	y ^= t
	z = bits.RotateLeft64(z, 45)
	rng.w, rng.x, rng.y, rng.z = w, x, y, z
	return r
}

// Intn produces an int in [0, n). Panics if n <= 0.
func (rng *RNG) Intn(n int) int {
	if n <= 0 {
		panic("xirho/xmath: rng.Intn max below zero")
	}
	bad := ^uint(0) - ^uint(0)%uint(n)
	x := uint(rng.Uint64())
	for x > bad {
		x = uint(rng.Uint64())
	}
	return int(x % uint(n))
}

// Jump quickly advances the generator by 2**192 steps.
func (rng *RNG) Jump() {
	var w, x, y, z uint64
	for _, j := range xoshiroJump {
		for i := 0; i < 64; i++ {
			if j&1 != 0 {
				w ^= rng.w
				x ^= rng.x
				y ^= rng.y
				z ^= rng.z
			}
			rng.Uint64()
			j >>= 1
		}
	}
	rng.w = w
	rng.x = x
	rng.y = y
	rng.z = z
}

var xoshiroJump = [4]uint64{
	0x76e15d3efefdcbbf, 0xc5004e441c522fb3, 0x77710069854ee241, 0x39109bb02acbe635,
}

// Uniform generates a number in the interval [0, 1).
func (rng *RNG) Uniform() float64 {
	return float64(rng.Uint64()&0x1fffffffffffff) * 1.11022302462515654042e-16
}

// Normal generates a variate from the standard normal distribution.
func (rng *RNG) Normal() float64 {
	// Ziggurat algorithm. Parameters are copied from
	// github.com/zephyrtronium/crazy, and the algorithm itself comes from
	// http://www.jstatsoft.org/v05/i08/paper.
	for {
		// Rather than reusing the low bits of a 32-bit random integer for the
		// index like in the original ziggurat paper, we can treat a 64-bit
		// integer as a 54-bit significand – giving us full precision and equal
		// distribution over float64 values – and a separate 10-bit index.
		j := int64(rng.Uint64())
		i := uint16(j & 0x3ff)
		j = j >> 10
		absj := j
		if absj < 0 {
			absj = -absj
		}
		x := float64(j) * normalW[i]
		if uint64(absj) < normalK[i] {
			return x
		}
		if i != 0 {
			if normalF[i]+rng.Uniform()*(normalF[i-1]-normalF[i]) < math.Exp(-0.5*x*x) {
				return x
			}
		} else {
			x = normalTail(rng)
			if j >= 0 {
				return x
			}
			return -x
		}
	}
}

func normalTail(rng *RNG) float64 {
	for {
		x := -math.Log(rng.Uniform()) / normalR
		y := -math.Log(rng.Uniform())
		if y+y >= x*x {
			return x + normalR
		}
	}
}

